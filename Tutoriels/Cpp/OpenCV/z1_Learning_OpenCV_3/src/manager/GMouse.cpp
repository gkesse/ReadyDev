//================================================
#include "GMouse.h"
//================================================
GMouse* GMouse::m_instance = 0;
//================================================
GMouse::GMouse() {
    m_event = -1;
    m_x = -1;
    m_y = -1;
}
//================================================
GMouse::~GMouse() {
	
}
//================================================
GMouse* GMouse::Instance() {
	if(m_instance == 0) {
        m_instance = new GMouse;
	}
	return m_instance;
}
//================================================
void GMouse::callback(int event, int x, int y) {
    m_event = event;
    m_x = x;
    m_y = y;
}
//================================================
int GMouse::getEvent() {
    return m_event;
}
//================================================
int GMouse::getX() {
    return m_x;
}
//================================================
int GMouse::getY() {
    return m_y;
}
//================================================
