#include "GDelay.h"
#include "GPortDef.h"
//===============================================
#define TIME_MS 110
//===============================================
void GDelay_ms(uint ms) {
	uint i, j;
    for(i = 0; i < ms; i++) {
        for(j = 0; j < TIME_MS; j++);
	}
}
